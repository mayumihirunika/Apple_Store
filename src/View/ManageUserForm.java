    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String userid = txtUserI.getText();
        String userName = txtUserN.getText();
        String password = new String(txtPassword.getPassword());
        String role = cmbRole.getSelectedItem().toString();

        if (userid.isEmpty() || userName.isEmpty() || password.isEmpty() || role.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Please fill in all the fields with valid data.");
        } else {
            if (manageuser.addUser(userid, userName, password, role)) {
                JOptionPane.showMessageDialog(null, "User added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

                // Clear
                txtUserI.setText("");
                txtUserN.setText("");
                txtPassword.setText("");
                cmbRole.setSelectedIndex(0);
                loadUserData();
            } else {
                JOptionPane.showMessageDialog(null, "Failed to add the User.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

