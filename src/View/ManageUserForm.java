    public ManageUserForm() {
        initComponents();
        manageuser = new ManageUserController();
        
        // Initialize your product table model
        userTable = new DefaultTableModel();
        userTable.addColumn(
                "user_id");
        userTable.addColumn(
                "user_name");
        userTable.addColumn(
                "user_password");
        userTable.addColumn(
                "Role(1=manager,0=cashier)");
        
        tblUser.setModel(userTable);
        loadUserData();
                tblUser.getSelectionModel()
                .addListSelectionListener(new ListSelectionListener() {
                    @Override
                    public void valueChanged(ListSelectionEvent e
                    ) {
                        if (!e.getValueIsAdjusting() && tblUser.getSelectedRow() != -1) {
                            int selectedRow = tblUser.getSelectedRow();

                            // Get the product data from the selected row
                            int userId = (int) userTable.getValueAt(selectedRow, 0);
                            String userName = (String) userTable.getValueAt(selectedRow, 1);
                            String userPassword = (String) userTable.getValueAt(selectedRow, 2);
                            int Role_as = (int) userTable.getValueAt(selectedRow, 3);

                            // Create a Product instance from the selected data
                            selectedUser = new User(userId, userName, userPassword, Role_as);
                        }
                    }
                }
                );
        
    }
    private void loadUserData() {
        List<User> users = manageuser.viewUserDetails(); // Retrieve user data

        // Clear existing data from the table
        userTable.setRowCount(0);

        for (User user : users) {
            // Add the user data to the table
            userTable.addRow(new Object[]{
                user.getUserId(),
                user.getUserName(),
                user.getUserPassword(),
                user.getRoleas()
            });
        }

    }


