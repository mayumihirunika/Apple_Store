    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchKey = txtSearch.getText();
        if(searchKey.isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter user name or password");
        }else{
            List <User> SearchResults = manageuser.searchUser(searchKey);
            userTable.setRowCount(0);
            
            for(User user : SearchResults){
                userTable.addRow(new Object []{
                    user.getUserId(),
                    user.getUserName(),
                    user.getUserPassword(),
                    user.getRoleas(),
                    
                });
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int userid = Integer.parseInt(txtUserI.getText());
        String userName = txtUserN.getText();
        String password = txtPassword.getText();
        String role = cmbRole.getSelectedItem().toString();
        
        if (manageuser.updateCurrentUser(userid, userName, password,role)) {
            JOptionPane.showMessageDialog(null, "updated successfully.");
            // Clear
            txtUserI.setText("");
            txtUserN.setText("");
            txtPassword.setText("");
            cmbRole.setSelectedIndex(0);
            loadUserData();
        } else {
            JOptionPane.showMessageDialog(null, "Failed to update the User.");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if (selectedUser != null) {
                    int userId = selectedUser.getUserId();
                    boolean removed = manageuser.removeUserFromDatabase(userId);
                    if (removed) {
                        JOptionPane.showMessageDialog(ManageUserForm.this, "Successfully Removed the User.", "Success", JOptionPane.INFORMATION_MESSAGE);
                        loadUserData();
                    } else {
                        JOptionPane.showMessageDialog(ManageUserForm.this, "Failed to remove the User.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
    }//GEN-LAST:event_btnRemoveActionPerformed

